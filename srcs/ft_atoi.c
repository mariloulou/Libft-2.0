int	ft_atoi(char *str)
{
	int i;
	int n;
	int ret;

	i = 0;
	ret = 0;
	while ((str[i] >= 9  && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret = ret * 10;
		ret = ret + (str[i] - 48);
		i++;
	}
	if (n == 1)
		return(-ret);
	return(ret);
}
